/*
Compilation and Execution in Java
Java follows a two-step process:

Step 1: Compilation
You run the Java compiler:

javac HelloWorld.java

Step 2: Execution
You then run the program using the Java Virtual Machine (JVM):

java HelloWorld

This tells the JVM to interpret the bytecode and execute the program.
*/
